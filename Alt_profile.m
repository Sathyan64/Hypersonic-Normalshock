function [P1,T1,rho1] = Alt_profile(h)
if h <= 11000
       T1 = 15.04 - 0.00649.*h;
       P1 = 101.29.*(((T1+273.1)/288.08).^5.256);
    elseif h > 11000 && h < 25000
       T1 = -56.46;
       P1 = 22.65.*exp(1.73-0.000157.*h);
    else
       T1 = -131.21 + 0.00299.*h;
       P1 = 2.488.*(((T1+273.1)/216.6).^-11.388);
    end 
 P1 = P1*1000; 
 T1 = T1+273.1;
 rho1 = P1/(287*T1);  
end
